        <h3 class="law-title">中华人民共和国刑法</h3>
        <p class="indent">（1979年7月1日第五届全国人民代表大会第二次会议通过，1997年3月14日第八届全国人民代表大会第五次会议修订。根据1999年12月25日中华人民共和国刑法修正案，2001年8月31日中华人民共和国刑法修正案(二)，2001年12月29日中华人民共和国刑法修正案(三)，2002年12 月28日中华人民共和国刑法修正案(四)，2005年2月28日中华人民共和国刑法修正案(五)，2006年6月29日中华人民共和国刑法修正案(六)，2009年2月28日中华人民共和国刑法修正案(七)修正，根据2009年8月27日《全国人民代表大会常务委员会关于修改部分法律的决定》修正，根据2011年2月25日中华人民共和国刑法修正案(八)修正，根据2015年8月29日第十二届全国人民代表大会常务委员会第十六次会议通过的《刑法修正案(九)》修正。）</p>
        <p class="bold indent center">第一编　总则</p>
        <p class="bold indent">第一章　刑法的任务、基本原则和适用范围</p>
        <p class="indent">第一条　【立法宗旨】为了惩罚犯罪，保护人民，根据宪法，结合我国同犯罪作斗争的具体 经验及实际情况，制定本法。</p>
        <p class="indent">第二条　【本法任务】中华人民共和国刑法的任务，是用刑罚同一切犯罪行为作斗争，以保卫国家安全，保卫人民民主专政的政权和社会主义制度，保护国有财产和劳动群众集体所有的财产，保护公民私人所有的财产，保护公民的人身权利、民主权利和其他权利，维护社会秩序、经济秩序，保障社会主义建设事业的顺利进行。</p>
        <p class="indent">第三条　【罪刑法定】法律明文规定为犯罪行为的，依照法律定罪处刑;法律没有明文规定为犯罪行为的，不得定罪处刑。</p>
        <p class="indent">第四条　【适用刑法人人平等】对任何人犯罪，在适用法律上一律平等。不允许任何人有超越法律的特权。</p>
        <p class="indent">第五条　【罪责刑相适应】刑罚的轻重，应当与犯罪分子所犯罪行和承担的刑事责任相适应。</p>
        <p class="indent">第六条　【属地管辖权】凡在中华人民共和国领域内犯罪的，除法律有特别规定的以外，都适用本法。</p>
        <p class="indent">凡在中华人民共和国船舶或者航空器内犯罪的，也适用本法。</p>
        <p class="indent">犯罪的行为或者结果有一项发生在中华人民共和国领域内的，就认为是在中华人民共和国领域内犯罪。tg</p>
        <p class="indent">第七条　【属人管辖权】中华人民共和国公民在中华人民共和国领域外犯本法规定之罪的，适用本法，但是按本法规定的最高刑为三年以下有期徒刑的，可以不予追究。</p>
        <p class="indent">中华人民共和国国家工作人员和军人在中华人民共和国领域外犯本法规定之罪的，适用本法。</p>
        <p class="indent">第八条　【保护管辖权】外国人在中华人民共和国领域外对中华人民共和国国家或者公民犯罪，而按本法规定的最低刑为三年以上有期徒刑的，可以适用本法，但是按照犯罪地的法律不受处罚的除外。</p>
        <p class="indent">第九条　【普遍管辖权】对于中华人民共和国缔结或者参加的国际条约所规定的罪行，中华人民共和国在所承担条约义务的范围内行使刑事管辖权的，适用本法。</p>
        <p class="indent">第十条　【对外国刑事判决的消极承认】凡在中华人民共和国领域外犯罪，依照本法应当负刑事责任的，虽然经过外国审判，仍然可以依照本法追究，但是在外国已经受过刑罚处罚的，可以免除或者减轻处罚。</p>
        <p class="indent">第十一条　【外交代表刑事管辖豁免】享有外交特权和豁免权的外国人的刑事责任，通过外交途径解决。</p>
        <p class="indent">第十二条　【刑法溯及力】中华人民共和国成立以后本法施行以前的行为，如果当时的法律不认为是犯罪的，适用当时的法律;如果当时的法律认为是犯罪的，依照本法总则第四章第八节的规定应当追诉的，按照当时的法律追究刑事责任，但是如果本法不认为是犯罪或者处刑较轻的，适用本法。</p>
        <p class="indent">本法施行以前，依照当时的法律已经作出的生效判决，继续有效。</p>
        <p class="bold indent">第二章　犯罪</p>
        <p class="indent">第一节　犯罪和刑事责任</p>
        <p class="indent">第十三条　【犯罪概念】一切危害国家主权、领土完整和安全，分裂国家、颠覆人民民主专政的政权和推翻社会主义制度，破坏社会秩序和经济秩序，侵犯国有财产或者劳动群众集体所有的财产，侵犯公民私人所有的财产，侵犯公民的人身权利、民主权利和其他权利，以及其他危害社会的行为，依照法律应当受刑罚处罚的，都是犯罪，但是情节显著轻微危害不大的，不认为是犯罪。</p>
        <p class="indent">第十四条　【故意犯罪】明知自己的行为会发生危害社会的结果，并且希望或者放任这种结果发生，因而构成犯罪的，是故意犯罪。</p>
        <p class="indent">故意犯罪，应当负刑事责任。</p>
        <p class="indent">第十五条　【过失犯罪】应当预见自己的行为可能发生危害社会的结果，因为疏忽大意而没有预见，或者已经预见而轻信能够避免，以致发生这种结果的，是过失犯罪。</p>
        <p class="indent">过失犯罪，法律有规定的才负刑事责任。</p>
        <p class="indent">第十六条　【不可抗力和意外事件】行为在客观上虽然造成了损害结果，但是不是出于故意或者过失，而是由于不能抗拒或者不能预见的原因所引起的，不是犯罪。</p>
        <p class="indent">第十七条　【刑事责任年龄】已满十六周岁的人犯罪，应当负刑事责任。</p>
        <p class="indent">已满十四周岁不满十六周岁的人，犯故意杀人、故意伤害致人重伤或者死亡、强奸、抢劫、贩卖毒品、放火、爆炸、投放危险物质罪的，应当负刑事责任。</p>
        <p class="indent">已满十四周岁不满十八周岁的人犯罪，应当从轻或者减轻处罚。</p>
        <p class="indent">因不满十六周岁不予刑事处罚的，责令他的家长或者监护人加以管教;在必要的时候，也可以由政府收容教养。</p>
        <p class="indent">第十七条之一 【刑事责任年龄】已满七十五周岁的人故意犯罪的，可以从轻或者减轻处罚;过失犯罪的，应当从轻或者减轻处罚。</p>
        <p class="indent">第十八条　【特殊人员的刑事责任能力】精神病人在不能辨认或者不能控制自己行为的时候造成危害结果，经法定程序鉴定确认的，不负刑事责任，但是应当责令他的家属或者监护人严加看管和医疗;在必要的时候，由政府强制医疗。</p>
        <p class="indent">间歇性的精神病人在精神正常的时候犯罪，应当负刑事责任。</p>
        <p class="indent">尚未完全丧失辨认或者控制自己行为能力的精神病人犯罪的，应当负刑事责任，但是可以从轻或者减轻处罚。</p>
        <p class="indent">醉酒的人犯罪，应当负刑事责任。</p>
        <p class="indent">第十九条　【又聋又哑的人或盲人犯罪的刑事责任】又聋又哑的人或者盲人犯罪，可以从轻、减轻或者免除处罚。</p>
        <p class="indent">第二十条　【正当防卫】为了使国家、公共利益、本人或者他人的人身、财产和其他权利免受正在进行的不法侵害，而采取的制止不法侵害的行为，对不法侵害人造成损害的，属于正当防卫，不负刑事责任。</p>
        <p class="indent">正当防卫明显超过必要限度造成重大损害的，应当负刑事责任，但是应当减轻或者免除处罚。</p>
        <p class="indent">对正在进行行凶、杀人、抢劫、强奸、绑架以及其他严重危及人身安全的暴力犯罪，采取防卫行为，造成不法侵害人伤亡的，不属于防卫过当，不负刑事责任。</p>
        <p class="indent">第二十一条　【紧急避险】为了使国家、公共利益、本人或者他人的人身、财产和其他权利免受正在发生的危险，不得已采取的紧急避险行为，造成损害的，不负刑事责任。</p>
        <p class="indent">紧急避险超过必要限度造成不应有的损害的，应当负刑事责任，但是应当减轻或者免除处罚。</p>
        <p class="indent">第一款中关于避免本人危险的规定，不适用于职务上、业务上负有特定责任的人。</p>
        <p class="indent">第二节　犯罪的预备、未遂和中止</p>
        <p class="indent">第二十二条　【犯罪预备】为了犯罪，准备工具、制造条件的，是犯罪预备。</p>
        <p class="indent">对于预备犯，可以比照既遂犯从轻、减轻处罚或者免除处罚。</p>
        <p class="indent">第二十三条　【犯罪未遂】已经着手实行犯罪，由于犯罪分子意志以外的原因而未得逞的，是犯罪未遂。</p>
        <p class="indent">对于未遂犯，可以比照既遂犯从轻或者减轻处罚。</p>
        <p class="indent">第二十四条　【犯罪中止】在犯罪过程中，自动放弃犯罪或者自动有效地防止犯罪结果发生的，是犯罪中止。</p>
        <p class="indent">对于中止犯，没有造成损害的，应当免除处罚;造成损害的，应当减轻处罚。</p>
        <p class="indent">第三节　共同犯罪</p>
        <p class="indent">第二十五条　【共同犯罪的概念】共同犯罪是指二人以上共同故意犯罪。</p>
        <p class="indent">二人以上共同过失犯罪，不以共同犯罪论处;应当负刑事责任的，按照他们所犯的罪分别处罚。</p>
        <p class="indent">第二十六条　【主犯】组织、领导犯罪集团进行犯罪活动的或者在共同犯罪中起主要作用的，是主犯。</p>
        <p class="indent">三人以上为共同实施犯罪而组成的较为固定的犯罪组织，是犯罪集团。</p>
        <p class="indent">对组织、领导犯罪集团的首要分子，按照集团所犯的全部罪行处罚。</p>
        <p class="indent">对于第三款规定以外的主犯，应当按照其所参与的或者组织、指挥的全部犯罪处罚。</p>
        <p class="indent">第二十七条　【从犯】在共同犯罪中起次要或者辅助作用的，是从犯。</p>
        <p id="aaa" class="indent">对于从犯，应当从轻、减轻处罚或者免除处罚。</p>
        <p class="indent">第二十八条　【胁从犯】对于被胁迫参加犯罪的，应当按照他的犯罪情节减轻处罚或者免除处罚。</p>
        <p class="indent">第二十九条　【教唆犯】教唆他人犯罪的，应当按照他在共同犯罪中所起的作用处罚。教唆不满十八周岁的人犯罪的，应当从重处罚。</p>
        <p class="indent">如果被教唆的人没有犯被教唆的罪，对于教唆犯，可以从轻或者减轻处罚。</p>
        <p class="indent">第四节　单位犯罪</p>
        <p class="indent">第三十条　【单位负刑事责任的范围】公司、企业、事业单位、机关、团体实施的危害社会的行为，法律规定为单位犯罪的，应当负刑事责任。</p>
        <p class="indent">第三十一条　【单位犯罪的处罚原则】单位犯罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员判处刑罚。本法分则和其他法律另有规定的，依照规定。</p>
        <p class="bold indent">第三章　刑罚</p>
        <p class="indent">第一节　刑罚的种类</p>
        <p class="indent">第三十二条　【主刑和附加刑】刑罚分为主刑和附加刑。</p>
        <p class="indent">第三十三条　【主刑种类】主刑的种类如下：</p>
        <p class="indent">(一)管制;</p>
        <p class="indent">(二)拘役;</p>
        <p class="indent">(三)有期徒刑;</p>
        <p class="indent">(四)无期徒刑;</p>
        <p class="indent">(五)死刑。</p>
        <p class="indent">第三十四条　【附加刑种类】附加刑的种类如下：</p>
        <p class="indent">(一)罚金;</p>
        <p class="indent">(二)剥夺政治权利;</p>
        <p class="indent">(三)没收财产。</p>
        <p class="indent">附加刑也可以独立适用。</p>
        <p class="indent">第三十五条　【驱逐出境】对于犯罪的外国人，可以独立适用或者附加适用驱逐出境。</p>
        <p class="indent">第三十六条　【赔偿经济损失与民事优先原则】由于犯罪行为而使被害人遭受经济损失的，对犯罪分子除依法给予刑事处罚外，并应根据情况判处赔偿经济损失。</p>
        <p class="indent">承担民事赔偿责任的犯罪分子，同时被判处罚金，其财产不足以全部支付的，或者被判处没收财产的，应当先承担对被害人的民事赔偿责任。</p>
        <p class="indent">第三十七条　【非刑罚性处置措施】对于犯罪情节轻微不需要判处刑罚的，可以免予刑事处罚，但是可以根据案件的不同情况，予以训诫或者责令具结悔过、赔礼道歉、赔偿损失，或者由主管部门予以行政处罚或者行政处分。</p>
        <p class="indent">第三十七条之一　【禁业规定】因利用职业便利实施犯罪，或者实施违背职业要求的特定义务的犯罪被判处刑罚的，人民法院可以根据犯罪情况和预防再犯罪的需要，禁止其自刑罚执行完毕之日或者假释之日起从事相关职业，期限为三年至五年。</p>
        <p class="indent">被禁止从事相关职业的人违反人民法院依照前款规定作出的决定的，由公安机关依法给予处罚;情节严重的，依照本法第三百一十三条的规定定罪处罚。</p>
        <p class="indent">其他法律、行政法规对其从事相关职业另有禁止或者限制性规定的，从其规定。</p>
        <p class="indent">第二节　管制</p>
        <p class="indent">第三十八条　【管制的期限与执行机关】管制的期限，为三个月以上二年以下。</p>
        <p class="indent">判处管制，可以根据犯罪情况，同时禁止犯罪分子在执行期间从事特定活动，进入特定区域、场所，接触特定的人。</p>
        <p class="indent">对判处管制的犯罪分子，依法实行社区矫正。</p>
        <p class="indent">违反第二款规定的禁止令的，由公安机关依照《中华人民共和国治安管理处罚法》的规定处罚。</p>
        <p class="indent">第三十九条　【被管制罪犯的义务与权利】被判处管制的犯罪分子，在执行期间，应当遵守下列规定：</p>
        <p class="indent">(一)遵守法律、行政法规，服从监督;</p>
        <p class="indent">(二)未经执行机关批准，不得行使言论、出版、集会、结社、游行、示威自由的权利;</p>
        <p class="indent">(三)按照执行机关规定报告自己的活动情况;</p>
        <p class="indent">(四)遵守执行机关关于会客的规定;</p>
        <p class="indent">(五)离开所居住的市、县或者迁居，应当报经执行机关批准。</p>
        <p class="indent">对于被判处管制的犯罪分子，在劳动中应当同工同酬。</p>
        <p class="indent">第四十条　【管制期满解除】被判处管制的犯罪分子，管制期满，执行机关应即向本人和其所在单位或者居住地的群众宣布解除管制。</p>
        <p class="indent">第四十一条　【管制刑期的计算和折抵】管制的刑期，从判决执行之日起计算;判决执行以前先行羁押的，羁押一日折抵刑期二日。</p>
        <p class="indent">第三节　拘役</p>
        <p class="indent">第四十二条　【拘役的期限】拘役的期限，为一个月以上六个月以下。</p>
        <p class="indent">第四十三条　【拘役的执行】被判处拘役的犯罪分子，由公安机关就近执行。</p>
        <p class="indent">在执行期间，被判处拘役的犯罪分子每月可以回家一天至两天;参加劳动的，可以酌量发给报酬。</p>
        <p class="indent">第四十四条　【拘役刑期的计算和折抵】拘役的刑期，从判决执行之日起计算;判决执行以前先行羁押的，羁押一日折抵刑期一日。</p>
        <p class="indent">第四节　有期徒刑、无期徒刑</p>
        <p class="indent">第四十五条　【有期徒刑的期限】有期徒刑的期限，除本法第五十条、第六十九条规定外，为六个月以上十五年以下。</p>
        <p class="indent">第四十六条　【有期徒刑与无期徒刑的执行】被判处有期徒刑、无期徒刑的犯罪分子，在监狱或者其他执行场所执行;凡有劳动能力的，都应当参加劳动，接受教育和改造。</p>
        <p class="indent">第四十七条　【有期徒刑刑期的计算与折抵】有期徒刑的刑期，从判决执行之日起计算;判决执行以前先行羁押的，羁押一日折抵刑期一日。</p>
        <p class="indent">第五节　死刑</p>
        <p class="indent">第四十八条　【死刑、死缓的适用对象及核准程序】死刑只适用于罪行极其严重的犯罪分子。对于应当判处死刑的犯罪分子，如果不是必须立即执行的，可以判处死刑同时宣告缓期二年执行。</p>
        <p class="indent">死刑除依法由最高人民法院判决的以外，都应当报请最高人民法院核准。死刑缓期执行的，可以由高级人民法院判决或者核准。</p>
        <p class="indent">第四十九条　【死刑适用对象的限制】犯罪的时候不满十八周岁的人和审判的时候怀孕的妇女，不适用死刑。</p>
        <p class="indent">审判的时候已满七十五周岁的人，不适用死刑，但以特别残忍手段致人死亡的除外。</p>
        <p class="indent">第五十条　【死缓变更】判处死刑缓期执行的，在死刑缓期执行期间，如果没有故意犯罪，二年期满以后，减为无期徒刑;如果确有重大立功表现，二年期满以后，减为二十五年有期徒刑;如果故意犯罪，情节恶劣的，报请最高人民法院核准后执行死刑;对于故意犯罪未执行死刑的，死刑缓期执行的期间重新计算，并报最高人民法院备案。</p>
        <p class="indent">对被判处死刑缓期执行的累犯以及因故意杀人、强奸、抢劫、绑架、放火、爆炸、投放危险物质或者有组织的暴力性犯罪被判处死刑缓期执行的犯罪分子，人民法院根据犯罪情节等情况可以同时决定对其限制减刑。</p>
        <p class="indent">第五十一条　【死缓期间及减为有期徒刑的刑期计算】死刑缓期执行的期间，从判决确定之日起计算。死刑缓期执行减为有期徒刑的刑期，从死刑缓期执行期满之日起计算。</p>
        <p class="indent">第六节　罚金</p>
        <p class="indent">第五十二条　【罚金数额的裁量】判处罚金，应当根据犯罪情节决定罚金数额。</p>
        <p class="indent">第五十三条　【罚金的缴纳】罚金在判决指定的期限内一次或者分期缴纳。期满不缴纳的，强制缴纳。对于不能全部缴纳罚金的，人民法院在任何时候发现被执行人有可以执行的财产，应当随时追缴。</p>
        <p class="indent">由于遭遇不能抗拒的灾祸等原因缴纳确实有困难的，经人民法院裁定，可以延期缴纳、酌情减少或者免除。</p>
        <p class="indent">第七节　剥夺政治权利</p>
        <p class="indent">第五十四条　【剥夺政治权利的含义】剥夺政治权利是剥夺下列权利：</p>
        <p class="indent">(一)选举权和被选举权;</p>
        <p class="indent">(二)言论、出版、集会、结社、游行、示威自由的权利;</p>
        <p class="indent">(三)担任国家机关职务的权利;</p>
        <p class="indent">(四)担任国有公司、企业、事业单位和人民团体领导职务的权利。</p>
        <p class="indent">第五十五条　【剥夺政治权利的期限】剥夺政治权利的期限，除本法第五十七条规定外，为一年以上五年以下。</p>
        <p class="indent">判处管制附加剥夺政治权利的，剥夺政治权利的期限与管制的期限相等，同时执行。</p>
        <p class="indent">第五十六条　【剥夺政治权利的附加、独立适用】对于危害国家安全的犯罪分子应当附加剥夺政治权利;对于故意杀人、强奸、放火、爆炸、投毒、抢劫等严重破坏社会秩序的犯罪分子，可以附加剥夺政治权利。</p>
        <p class="indent">独立适用剥夺政治权利的，依照本法分则的规定。</p>
        <p class="indent">第五十七条　【对死刑、无期徒刑罪犯剥夺政治权利的适用】对于被判处死刑、无期徒刑的犯罪分子，应当剥夺政治权利终身。</p>
        <p class="indent">在死刑缓期执行减为有期徒刑或者无期徒刑减为有期徒刑的时候，应当把附加剥夺政治权利的期限改为三年以上十年以下。</p>
        <p class="indent">第五十八条　【剥夺政治权利的刑期计算、效力与执行】附加剥夺政治权利的刑期，从徒刑、拘役执行完毕之日或者从假释之日起计算;剥夺政治权利的效力当然施用于主刑执行期间。</p>
        <p class="indent">被剥夺政治权利的犯罪分子，在执行期间，应当遵守法律、行政法规和国务院公安部门有关监督管理的规定，服从监督;不得行使本法第五十四条规定的各项权利。</p>
        <p class="indent">第八节　没收财产</p>
        <p class="indent">第五十九条　【没收财产的范围】没收财产是没收犯罪分子个人所有财产的一部或者全部。没收全部财产的，应当对犯罪分子个人及其扶养的家属保留必需的生活费用。</p>
        <p class="indent">在判处没收财产的时候，不得没收属于犯罪分子家属所有或者应有的财产。</p>
        <p class="indent">第六十条　【以没收的财产偿还债务】没收财产以前犯罪分子所负的正当债务，需要以没收的财产偿还的，经债权人请求，应当偿还。</p>
        <p class="bold indent">第四章　刑罚的具体运用</p>
        <p class="indent">第一节　量刑</p>
        <p class="indent">第六十一条　【量刑的一般原则】对于犯罪分子决定刑罚的时候，应当根据犯罪的事实、犯罪的性质、情节和对于社会的危害程度，依照本法的有关规定判处。</p>
        <p class="indent">第六十二条　【从重处罚与从轻处罚】犯罪分子具有本法规定的从重处罚、从轻处罚情节的，应当在法定刑的限度以内判处刑罚。</p>
        <p class="indent">第六十三条　【减轻处罚】犯罪分子具有本法规定的减轻处罚情节的，应当在法定刑以下判处刑罚;本法规定有数个量刑幅度的，应当在法定量刑幅度的下一个量刑幅度内判处刑罚。</p>
        <p class="indent">犯罪分子虽然不具有本法规定的减轻处罚情节，但是根据案件的特殊情况，经最高人民法院核准，也可以在法定刑以下判处刑罚。</p>
        <p class="indent">第六十四条　【犯罪物品的处理】犯罪分子违法所得的一切财物，应当予以追缴或者责令退赔;对被害人的合法财产，应当及时返还;违禁品和供犯罪所用的本人财物，应当予以没收。没收的财物和罚金，一律上缴国库，不得挪用和自行处理。</p>
        <p class="indent">第二节　累犯</p>
        <p class="indent">第六十五条　【一般累犯】被判处有期徒刑以上刑罚的犯罪分子，刑罚执行完毕或者赦免以后，在五年以内再犯应当判处有期徒刑以上刑罚之罪的，是累犯，应当从重处罚，但是过失犯罪和不满十八周岁的人犯罪的除外。</p>
        <p class="indent">前款规定的期限，对于被假释的犯罪分子，从假释期满之日起计算。</p>
        <p class="indent">第六十六条　【特别累犯】危害国家安全犯罪、恐怖活动犯罪、黑社会性质的组织犯罪的犯罪分子，在刑罚执行完毕或者赦免以后，在任何时候再犯上述任一类罪的，都以累犯论处。</p>
        <p class="indent">第三节　自首和立功</p>
        <p class="indent">第六十七条　【自首】犯罪以后自动投案，如实供述自己的罪行的，是自首。对于自首的犯罪分子，可以从轻或者减轻处罚。其中，犯罪较轻的，可以免除处罚。</p>
        <p class="indent">被采取强制措施的犯罪嫌疑人、被告人和正在服刑的罪犯，如实供述司法机关还未掌握的本人其他罪行的，以自首论。</p>
        <p class="indent">犯罪嫌疑人虽不具有前两款规定的自首情节，但是如实供述自己罪行的，可以从轻处罚;因其如实供述自己罪行，避免特别严重后果发生的，可以减轻处罚。</p>
        <p class="indent">第六十八条　【立功】犯罪分子有揭发他人犯罪行为，查证属实的，或者提供重要线索，从而得以侦破其他案件等立功表现的，可以从轻或者减轻处罚;有重大立功表现的，可以减轻或者免除处罚。</p>
        <p class="indent">第四节　数罪并罚</p>
        <p class="indent">第六十九条　【数罪并罚的一般原则】判决宣告以前一人犯数罪的，除判处死刑和无期徒刑的以外，应当在总和刑期以下、数刑中最高刑期以上，酌情决定执行的刑期，但是管制最高不能超过三年，拘役最高不能超过一年，有期徒刑总和刑期不满三十五年的，最高不能超过二十年，总和刑期在三十五年以上的，最高不能超过二十五年。</p>
        <p class="indent">数罪中有判处有期徒刑和拘役的，执行有期徒刑。数罪中有判处有期徒刑和管制，或者拘役和管制的，有期徒刑、拘役执行完毕后，管制仍须执行。</p>
        <p class="indent">数罪中有判处附加刑的，附加刑仍须执行，其中附加刑种类相同的，合并执行，种类不同的，分别执行。</p>
        <p class="indent">第七十条　【判决宣告后发现漏罪的并罚】判决宣告以后，刑罚执行完毕以前，发现被判刑的犯罪分子在判决宣告以前还有其他罪没有判决的，应当对新发现的罪作出判决，把前后两个判决所判处的刑罚，依照本法第六十九条的规定，决定执行的刑罚。已经执行的刑期，应当计算在新判决决定的刑期以内。</p>
        <p class="indent">第七十一条　【判决宣告后又犯新罪的并罚】判决宣告以后，刑罚执行完毕以前，被判刑的犯罪分子又犯罪的，应当对新犯的罪作出判决，把前罪没有执行的刑罚和后罪所判处的刑罚，依照本法第六十九条的规定，决定执行的刑罚。</p>
        <p class="indent">第五节　缓刑</p>
        <p class="indent">第七十二条　【适用条件】对于被判处拘役、三年以下有期徒刑的犯罪分子，同时符合下列条件的，可以宣告缓刑，对其中不满十八周岁的人、怀孕的妇女和已满七十五周岁的人，应当宣告缓刑：</p>
        <p class="indent">(一)犯罪情节较轻;</p>
        <p class="indent">(二)有悔罪表现;</p>
        <p class="indent">(三)没有再犯罪的危险;</p>
        <p class="indent">(四)宣告缓刑对所居住社区没有重大不良影响。</p>
        <p class="indent">宣告缓刑，可以根据犯罪情况，同时禁止犯罪分子在缓刑考验期限内从事特定活动，进入特定区域、场所，接触特定的人。</p>
        <p class="indent">被宣告缓刑的犯罪分子，如果被判处附加刑，附加刑仍须执行。</p>
        <p class="indent">第七十三条　【考验期限】拘役的缓刑考验期限为原判刑期以上一年以下，但是不能少于二个月。</p>
        <p class="indent">有期徒刑的缓刑考验期限为原判刑期以上五年以下，但是不能少于一年。</p>
        <p class="indent">缓刑考验期限，从判决确定之日起计算。</p>
        <p class="indent">第七十四条　【累犯不适用缓刑】对于累犯和犯罪集团的首要分子，不适用缓刑。</p>
        <p class="indent">第七十五条　【缓刑犯应遵守的规定】被宣告缓刑的犯罪分子，应当遵守下列规定：</p>
        <p class="indent">(一)遵守法律、行政法规，服从监督;</p>
        <p class="indent">(二)按照考察机关的规定报告自己的活动情况;</p>
        <p class="indent">(三)遵守考察机关关于会客的规定;</p>
        <p class="indent">(四)离开所居住的市、县或者迁居，应当报经考察机关批准。</p>
        <p class="indent">第七十六条　【缓刑的考验及其积极后果】对宣告缓刑的犯罪分子，在缓刑考验期限内，依法实行社区矫正，如果没有本法第七十七条规定的情形，缓刑考验期满，原判的刑罚就不再执行，并公开予以宣告。</p>
        <p class="indent">第七十七条　【缓刑的撤销及其处理】被宣告缓刑的犯罪分子，在缓刑考验期限内犯新罪或者发现判决宣告以前还有其他罪没有判决的，应当撤销缓刑，对新犯的罪或者新发现的罪作出判决，把前罪和后罪所判处的刑罚，依照本法第六十九条的规定，决定执行的刑罚。</p>
        <p class="indent">被宣告缓刑的犯罪分子，在缓刑考验期限内，违反法律、行政法规或者国务院有关部门关于缓刑的监督管理规定，或者违反人民法院判决中的禁止令，情节严重的，应当撤销缓刑，执行原判刑罚。</p>
        <p class="indent">第六节　减刑</p>
        <p class="indent">第七十八条　【减刑条件与限度】被判处管制、拘役、有期徒刑、无期徒刑的犯罪分子，在执行期间，如果认真遵守监规，接受教育改造，确有悔改表现的，或者有立功表现的，可以减刑;有下列重大立功表现之一的，应当减刑：</p>
        <p class="indent">(一)阻止他人重大犯罪活动的;</p>
        <p class="indent">(二)检举监狱内外重大犯罪活动，经查证属实的;</p>
        <p class="indent">(三)有发明创造或者重大技术革新的;</p>
        <p class="indent">(四)在日常生产、生活中舍己救人的;</p>
        <p class="indent">(五)在抗御自然灾害或者排除重大事故中，有突出表现的;</p>
        <p class="indent">(六)对国家和社会有其他重大贡献的。</p>
        <p class="indent">减刑以后实际执行的刑期不能少于下列期限：</p>
        <p class="indent">(一)判处管制、拘役、有期徒刑的，不能少于原判刑期的二分之一;</p>
        <p class="indent">(二)判处无期徒刑的，不能少于十三年;</p>
        <p class="indent">(三)人民法院依照本法第五十条第二款规定限制减刑的死刑缓期执行的犯罪分子，缓期执行期满后依法减为无期徒刑的，不能少于二十五年，缓期执行期满后依法减为二十五年有期徒刑的，不能少于二十年。</p>
        <p class="indent">第七十九条　【减刑程序】对于犯罪分子的减刑，由执行机关向中级以上人民法院提出减刑建议书。人民法院应当组成合议庭进行审理，对确有悔改或者立功事实的，裁定予以减刑。非经法定程序不得减刑。</p>
        <p class="indent">第八十条　【无期徒刑减刑的刑期计算】无期徒刑减为有期徒刑的刑期，从裁定减刑之日起计算。</p>
        <p class="indent">第七节　假释</p>
        <p class="indent">第八十一条　【假释的适用条件】被判处有期徒刑的犯罪分子，执行原判刑期二分之一以上，被判处无期徒刑的犯罪分子，实际执行十三年以上，如果认真遵守监规，接受教育改造，确有悔改表现，没有再犯罪的危险的，可以假释。如果有特殊情况，经最高人民法院核准，可以不受上述执行刑期的限制。</p>
        <p class="indent">对累犯以及因故意杀人、强奸、抢劫、绑架、放火、爆炸、投放危险物质或者有组织的暴力性犯罪被判处十年以上有期徒刑、无期徒刑的犯罪分子，不得假释。</p>
        <p class="indent">对犯罪分子决定假释时，应当考虑其假释后对所居住社区的影响。</p>
        <p class="indent">第八十二条　【假释的程序】对于犯罪分子的假释，依照本法第七十九条规定的程序进行。非经法定程序不得假释。</p>
        <p class="indent">第八十三条　【假释的考验期限】有期徒刑的假释考验期限，为没有执行完毕的刑期;无期徒刑的假释考验期限为十年。</p>
        <p class="indent">假释考验期限，从假释之日起计算。</p>
        <p class="indent">第八十四条　【假释犯应遵守的规定】被宣告假释的犯罪分子，应当遵守下列规定：</p>
        <p class="indent">(一)遵守法律、行政法规，服从监督;</p>
        <p class="indent">(二)按照监督机关的规定报告自己的活动情况;</p>
        <p class="indent">(三)遵守监督机关关于会客的规定;</p>
        <p class="indent">(四)离开所居住的市、县或者迁居，应当报经监督机关批准。</p>
        <p class="indent">第八十五条　【假释考验及其积极后果】对假释的犯罪分子，在假释考验期限内，依法实行社区矫正，如果没有本法第八十六条规定的情形，假释考验期满，就认为原判刑罚已经执行完毕，并公开予以宣告。</p>
        <p class="indent">第八十六条　【假释的撤销及其处理】被假释的犯罪分子，在假释考验期限内犯新罪，应当撤销假释，依照本法第七十一条的规定实行数罪并罚。</p>
        <p class="indent">在假释考验期限内，发现被假释的犯罪分子在判决宣告以前还有其他罪没有判决的，应当撤销假释，依照本法第七十条的规定实行数罪并罚。</p>
        <p class="indent">被假释的犯罪分子，在假释考验期限内，有违反法律、行政法规或者国务院有关部门关于假释的监督管理规定的行为，尚未构成新的犯罪的，应当依照法定程序撤销假释，收监执行未执行完毕的刑罚。</p>
        <p class="indent">第八节　时效</p>
        <p class="indent">第八十七条　【追诉时效期限】犯罪经过下列期限不再追诉：</p>
        <p class="indent">(一)法定最高刑为不满五年有期徒刑的，经过五年;</p>
        <p class="indent">(二)法定最高刑为五年以上不满十年有期徒刑的，经过十年;</p>
        <p class="indent">(三)法定最高刑为十年以上有期徒刑的，经过十五年;</p>
        <p class="indent">(四)法定最高刑为无期徒刑、死刑的，经过二十年。如果二十年以后认为必须追诉的，须报请最高人民检察院核准。</p>
        <p class="indent">第八十八条　【追诉期限的延长】在人民检察院、公安机关、国家安全机关立案侦查或者在人民法院受理案件以后，逃避侦查或者审判的，不受追诉期限的限制。</p>
        <p class="indent">被害人在追诉期限内提出控告，人民法院、人民检察院、公安机关应当立案而不予立案的，不受追诉期限的限制。</p>
        <p class="indent">第八十九条　【追诉期限的计算与中断】追诉期限从犯罪之日起计算;犯罪行为有连续或者继续状态的，从犯罪行为终了之日起计算。</p>
        <p class="indent">在追诉期限以内又犯罪的，前罪追诉的期限从犯后罪之日起计算。</p>
        <p class="bold indent">第五章　其他规定</p>
        <p class="indent">第九十条　【民族自治地方刑法适用的变通】民族自治地方不能全部适用本法规定的，可以由自治区或者省的人民代表大会根据当地民族的政治、经济、文化的特点和本法规定的基本原则，制定变通或者补充的规定，报请全国人民代表大会常务委员会批准施行。</p>
        <p class="indent">第九十一条　【公共财产的范围】本法所称公共财产，是指下列财产：</p>
        <p class="indent">(一)国有财产;</p>
        <p class="indent">(二)劳动群众集体所有的财产;</p>
        <p class="indent">(三)用于扶贫和其他公益事业的社会捐助或者专项基金的财产。</p>
        <p class="indent">在国家机关、国有公司、企业、集体企业和人民团体管理、使用或者运输中的私人财产，以公共财产论。</p>
        <p class="indent">第九十二条　【公民私人所有财产的范围】本法所称公民私人所有的财产，是指下列财产：</p>
        <p class="indent">(一)公民的合法收入、储蓄、房屋和其他生活资料;</p>
        <p class="indent">(二)依法归个人、家庭所有的生产资料;</p>
        <p class="indent">(三)个体户和私营企业的合法财产;</p>
        <p class="indent">(四)依法归个人所有的股份、股票、债券和其他财产。</p>
        <p class="indent">第九十三条　【国家工作人员的范围】本法所称国家工作人员，是指国家机关中从事公务的人员。</p>
        <p class="indent">国有公司、企业、事业单位、人民团体中从事公务的人员和国家机关、国有公司、企业、事业单位委派到非国有公司、企业、事业单位、社会团体从事公务的人员，以及其他依照法律从事公务的人员，以国家工作人员论。</p>
        <p class="indent">第九十四条　【司法工作人员的范围】本法所称司法工作人员，是指有侦查、检察、审判、监管职责的工作人员。</p>
        <p class="indent">第九十五条　【重伤】本法所称重伤，是指有下列情形之一的伤害：</p>
        <p class="indent">(一)使人肢体残废或者毁人容貌的;</p>
        <p class="indent">(二)使人丧失听觉、视觉或者其他器官机能的;</p>
        <p class="indent">(三)其他对于人身健康有重大伤害的。</p>
        <p class="indent">第九十六条　【违反国家规定之含义】本法所称违反国家规定，是指违反全国人民代表大会及其常务委员会制定的法律和决定，国务院制定的行政法规、规定的行政措施、发布的决定和命令。</p>
        <p class="indent">第九十七条　【首要分子的范围】本法所称首要分子，是指在犯罪集团或者聚众犯罪中起组织、策划、指挥作用的犯罪分子。</p>
        <p class="indent">第九十八条　【告诉才处理的含义】本法所称告诉才处理，是指被害人告诉才处理。如果被害人因受强制、威吓无法告诉的，人民检察院和被害人的近亲属也可以告诉。</p>
        <p class="indent">第九十九条　【以上、以下、以内之界定】本法所称以上、以下、以内，包括本数。</p>
        <p class="indent">第一百条　【前科报告制度】依法受过刑事处罚的人，在入伍、就业的时候，应当如实向有关单位报告自己曾受过刑事处罚，不得隐瞒。</p>
        <p class="indent">犯罪的时候不满十八周岁被判处五年有期徒刑以下刑罚的人，免除前款规定的报告义务。</p>
        <p class="indent">第一百零一条　【总则的效力】本法总则适用于其他有刑罚规定的法律，但是其他法律有特别规定的除外。</p>
        <p class="bold indent center">第二编　分则</p>
        <p class="bold indent">第一章　危害国家安全罪</p>
        <p class="indent">第一百零二条　【背叛国家罪】勾结外国，危害中华人民共和国的主权、领土完整和安全的，处无期徒刑或者十年以上有期徒刑。</p>
        <p class="indent">与境外机构、组织、个人相勾结，犯前款罪的，依照前款的规定处罚。</p>
        <p class="indent">第一百零三条　【分裂国家罪】组织、策划、实施分裂国家、破坏国家统一的，对首要分子或者罪行重大的，处无期徒刑或者十年以上有期徒刑;对积极参加的，处三年以上十年以下有期徒刑;对其他参加的，处三年以下有期徒刑、拘役、管制或者剥夺政治权利。</p>
        <p class="indent">【煽动分裂国家罪】煽动分裂国家、破坏国家统一的，处五年以下有期徒刑、拘役、管制或者剥夺政治权利;首要分子或者罪行重大的，处五年以上有期徒刑。</p>
        <p class="indent">第一百零四条　【武装叛乱、暴乱罪】组织、策划、实施武装叛乱或者武装暴乱的，对首要分子或者罪行重大的，处无期徒刑或者十年以上有期徒刑;对积极参加的，处三年以上十年以下有期徒刑;对其他参加的，处三年以下有期徒刑、拘役、管制或者剥夺政治权利。</p>
        <p class="indent">策动、胁迫、勾引、收买国家机关工作人员、武装部队人员、人民警察、民兵进行武装叛乱或者武装暴乱的，依照前款的规定从重处罚。</p>
        <p class="indent">第一百零五条　【颠覆国家政权罪】组织、策划、实施颠覆国家政权、推翻社会主义制度的，对首要分子或者罪行重大的，处无期徒刑或者十年以上有期徒刑;对积极参加的，处三年以上十年以下有期徒刑;对其他参加的，处三年以下有期徒刑、拘役、管制或者剥夺政治权利。</p>
        <p class="indent">【煽动颠覆国家政权罪】以造谣、诽谤或者其他方式煽动颠覆国家政权、推翻社会主义制度的，处五年以下有期徒刑、拘役、管制或者剥夺政治权利;首要分子或者罪行重大的，处五年以上有期徒刑。</p>
        <p class="indent">第一百零六条　【与境外勾结的处罚规定】与境外机构、组织、个人相勾结，实施本章第一百零三条、第一百零四条、第一百零五条规定之罪的，依照各该条的规定从重处罚。</p>
        <p class="indent">第一百零七条　【资助危害国家安全犯罪活动罪】境内外机构、组织或者个人资助实施本章第一百零二条、第一百零三条、第一百零四条、第一百零五条规定之罪的，对直接责任人员，处五年以下有期徒刑、拘役、管制或者剥夺政治权利;情节严重的，处五年以上有期徒刑。</p>
        <p class="indent">第一百零八条　【投敌叛变罪】投敌叛变的，处三年以上十年以下有期徒刑;情节严重或者带领武装部队人员、人民警察、民兵投敌叛变的，处十年以上有期徒刑或者无期徒刑。</p>
        <p class="indent">第一百零九条　【叛逃罪】国家机关工作人员在履行公务期间，擅离岗位，叛逃境外或者在境外叛逃的，处五年以下有期徒刑、拘役、管制或者剥夺政治权利;情节严重的，处五年以上十年以下有期徒刑。</p>
        <p class="indent">掌握国家秘密的国家工作人员叛逃境外或者在境外叛逃的，依照前款的规定从重处罚。</p>
        <p class="indent">第一百一十条　【间谍罪】有下列间谍行为之一，危害国家安全的，处十年以上有期徒刑或者无期徒刑;情节较轻的，处三年以上十年以下有期徒刑：</p>
        <p class="indent">(一)参加间谍组织或者接受间谍组织及其代理人的任务的;</p>
        <p class="indent">(二)为敌人指示轰击目标的。</p>
        <p class="indent">第一百一十一条　【为境外窃取、刺探、收买、非法提供国家秘密、情报罪】为境外的机构、组织、人员窃取、刺探、收买、非法提供国家秘密或者情报的，处五年以上十年以下有期徒刑;情节特别严重的，处十年以上有期徒刑或者无期徒刑;情节较轻的，处五年以下有期徒刑、拘役、管制或者剥夺政治权利。</p>
        <p class="indent">第一百一十二条　【资敌罪】战时供给敌人武器装备、军用物资资敌的，处十年以上有期徒刑或者无期徒刑;情节较轻的，处三年以上十年以下有期徒刑。</p>
        <p class="indent">第一百一十三条　【危害国家安全罪适用死刑、没收财产的规定】本章上述危害国家安全罪行中，除第一百零三条第二款、第一百零五条、第一百零七条、第一百零九条外，对国家和人民危害特别严重、情节特别恶劣的，可以判处死刑。</p>
        <p class="indent">犯本章之罪的，可以并处没收财产。</p>
        <p class="bold indent">第二章　危害公共安全罪</p>
        <p class="indent">第一百一十四条　【放火罪】【决水罪】【爆炸罪】【投放危险物质罪】【以危险方法危害公共安全罪】放火、决水、爆炸以及投放毒害性、放射性、传染病病原体等物质或者以其他危险方法危害公共安全，尚未造成严重后果的，处三年以上十年以下有期徒刑。</p>
        <p class="indent">第一百一十五条　【放火罪】【决水罪】【爆炸罪】【投放危险物质罪】【以危险方法危害公共安全罪】放火、决水、爆炸以及投放毒害性、放射性、传染病病原体等物质或者以其他危险方法致人重伤、死亡或者使公私财产遭受重大损失的，处十年以上有期徒刑、无期徒刑或者死刑。</p>
        <p class="indent">【失火罪】【过失决水罪】【过失爆炸罪】【过失投放危险物质罪】【过失以危险方法危害公共安全罪】过失犯前款罪的，处三年以上七年以下有期徒刑;情节较轻的，处三年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百一十六条　【破坏交通工具罪】破坏火车、汽车、电车、船只、航空器，足以使火车、汽车、电车、船只、航空器发生倾覆、毁坏危险，尚未造成严重后果的，处三年以上十年以下有期徒刑。</p>
        <p class="indent">第一百一十七条　【破坏交通设施罪】破坏轨道、桥梁、隧道、公路、机场、航道、灯塔、标志或者进行其他破坏活动，足以使火车、汽车、电车、船只、航空器发生倾覆、毁坏危险，尚未造成严重后果的，处三年以上十年以下有期徒刑。</p>
        <p class="indent">第一百一十八条　【破坏电力设备罪】【破坏易燃易爆设备罪】破坏电力、燃气或者其他易燃易爆设备，危害公共安全，尚未造成严重后果的，处三年以上十年以下有期徒刑。</p>
        <p class="indent">第一百一十九条　【破坏交通工具罪】【破坏交通设施罪】【破坏电力设备罪】【破坏易燃易爆设备罪】破坏交通工具、交通设施、电力设备、燃气设备、易燃易爆设备，造成严重后果的，处十年以上有期徒刑、无期徒刑或者死刑。</p>
        <p class="indent">【过失损坏交通工具罪】【过失损坏交通设施罪】【过失损坏电力设备罪】【过失损坏易燃易爆设备罪】过失犯前款罪的，处三年以上七年以下有期徒刑;情节较轻的，处三年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百二十条　【组织、领导、参加恐怖组织罪】组织、领导恐怖活动组织的，处十年以上有期徒刑或者无期徒刑，并处没收财产;积极参加的，处三年以上十年以下有期徒刑，并处罚金;其他参加的，处三年以下有期徒刑、拘役、管制或者剥夺政治权利，可以并处罚金。</p>
        <p class="indent">犯前款罪并实施杀人、爆炸、绑架等犯罪的，依照数罪并罚的规定处罚。</p>
        <p class="indent">第一百二十条之一　【帮助恐怖活动罪】资助恐怖活动组织、实施恐怖活动的个人的，或者资助恐怖活动培训的，处五年以下有期徒刑、拘役、管制或者剥夺政治权利，并处罚金;情节严重的，处五年以上有期徒刑，并处罚金或者没收财产。</p>
        <p class="indent">为恐怖活动组织、实施恐怖活动或者恐怖活动培训招募、运送人员的，依照前款的规定处罚。</p>
        <p class="indent">单位犯前两款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照第一款的规定处罚。</p>
        <p class="indent">第一百二十条之二　【准备实施恐怖活动罪】有下列情形之一的，处五年以下有期徒刑、拘役、管制或者剥夺政治权利，并处罚金;情节严重的，处五年以上有期徒刑，并处罚金或者没收财产：</p>
        <p class="indent">(一)为实施恐怖活动准备凶器、危险物品或者其他工具的;</p>
        <p class="indent">(二)组织恐怖活动培训或者积极参加恐怖活动培训的;</p>
        <p class="indent">(三)为实施恐怖活动与境外恐怖活动组织或者人员联络的;</p>
        <p class="indent">(四)为实施恐怖活动进行策划或者其他准备的。</p>
        <p class="indent">有前款行为，同时构成其他犯罪的，依照处罚较重的规定定罪处罚。</p>
        <p class="indent">第一百二十条之三　【宣扬恐怖主义、极端主义、煽动实施恐怖活动罪】以制作、散发宣扬恐怖主义、极端主义的图书、音频视频资料或者其他物品，或者通过讲授、发布信息等方式宣扬恐怖主义、极端主义的，或者煽动实施恐怖活动的，处五年以下有期徒刑、拘役、管制或者剥夺政治权利，并处罚金;情节严重的，处五年以上有期徒刑，并处罚金或者没收财产。</p>
        <p class="indent">第一百二十条之四　【利用极端主义破坏法律实施罪】利用极端主义煽动、胁迫群众破坏国家法律确立的婚姻、司法、教育、社会管理等制度实施的，处三年以下有期徒刑、拘役或者管制，并处罚金;情节严重的，处三年以上七年以下有期徒刑，并处罚金;情节特别严重的，处七年以上有期徒刑，并处罚金或者没收财产。</p>
        <p class="indent">第一百二十条之五　【强制穿戴宣扬恐怖主义、极端主义服饰、标志罪】以暴力、胁迫等方式强制他人在公共场所穿着、佩戴宣扬恐怖主义、极端主义服饰、标志的，处三年以下有期徒刑、拘役或者管制，并处罚金。</p>
        <p class="indent">第一百二十条之六　【非法持有宣扬恐怖主义、极端主义物品罪】明知是宣扬恐怖主义、极端主义的图书、音频视频资料或者其他物品而非法持有，情节严重的，处三年以下有期徒刑、拘役或者管制，并处或者单处罚金。</p>
        <p class="indent">第一百二十一条　【劫持航空器罪】以暴力、胁迫或者其他方法劫持航空器的，处十年以上有期徒刑或者无期徒刑;致人重伤、死亡或者使航空器遭受严重破坏的，处死刑。</p>
        <p class="indent">第一百二十二条　【劫持船只、汽车罪】以暴力、胁迫或者其他方法劫持船只、汽车的，处五年以上十年以下有期徒刑;造成严重后果的，处十年以上有期徒刑或者无期徒刑。</p>
        <p class="indent">第一百二十三条　【暴力危及飞行安全罪】对飞行中的航空器上的人员使用暴力，危及飞行安全，尚未造成严重后果的，处五年以下有期徒刑或者拘役;造成严重后果的，处五年以上有期徒刑。</p>
        <p class="indent">第一百二十四条　【破坏广播电视设施、公用电信设施罪】破坏广播电视设施、公用电信设施，危害公共安全的，处三年以上七年以下有期徒刑;造成严重后果的，处七年以上有期徒刑。</p>
        <p class="indent">【过失损坏广播电视设施、公用电信设施罪】过失犯前款罪的，处三年以上七年以下有期徒刑;情节较轻的，处三年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百二十五条　【非法制造、买卖、运输、邮寄、储存枪支、弹药、爆炸物罪】非法制造、买卖、运输、邮寄、储存枪支、弹药、爆炸物的，处三年以上十年以下有期徒刑;情节严重的，处十年以上有期徒刑、无期徒刑或者死刑。</p>
        <p class="indent">【非法制造、买卖、运输、储存危险物质罪】非法制造、买卖、运输、储存毒害性、放射性、传染病病原体等物质，危害公共安全的，依照前款的规定处罚。</p>
        <p class="indent">单位犯前两款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照第一款的规定处罚。</p>
        <p class="indent">第一百二十六条　【违规制造、销售枪支罪】依法被指定、确定的枪支制造企业、销售企业，违反枪支管理规定，有下列行为之一的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑;情节严重的，处五年以上十年以下有期徒刑;情节特别严重的，处十年以上有期徒刑或者无期徒刑：</p>
        <p class="indent">(一)以非法销售为目的，超过限额或者不按照规定的品种制造、配售枪支的;</p>
        <p class="indent">(二)以非法销售为目的，制造无号、重号、假号的枪支的;</p>
        <p class="indent">(三)非法销售枪支或者在境内销售为出口制造的枪支的。</p>
        <p class="indent">第一百二十七条　【盗窃、抢夺枪支、弹药、爆炸物、危险物质罪】盗窃、抢夺枪支、弹药、爆炸物的，或者盗窃、抢夺毒害性、放射性、传染病病原体等物质，危害公共安全的，处三年以上十年以下有期徒刑;情节严重的，处十年以上有期徒刑、无期徒刑或者死刑。</p>
        <p class="indent">【抢劫枪支、弹药、爆炸物、危险物质罪】【盗窃、抢夺枪支、弹药、爆炸物、危险物质罪】抢劫枪支、弹药、爆炸物的，或者抢劫毒害性、放射性、传染病病原体等物质，危害公共安全的，或者盗窃、抢夺国家机关、军警人员、民兵的枪支、弹药、爆炸物的，处十年以上有期徒刑、无期徒刑或者死刑。</p>
        <p class="indent">第一百二十八条　【非法持有、私藏枪支、弹药罪】违反枪支管理规定，非法持有、私藏枪支、弹药的，处三年以下有期徒刑、拘役或者管制;情节严重的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">【非法出租、出借枪支罪】依法配备公务用枪的人员，非法出租、出借枪支的，依照前款的规定处罚。</p>
        <p class="indent">【非法出租、出借枪支罪】依法配置枪支的人员，非法出租、出借枪支，造成严重后果的，依照第一款的规定处罚。</p>
        <p class="indent">单位犯第二款、第三款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照第一款的规定处罚。</p>
        <p class="indent">第一百二十九条　【丢失枪支不报罪】依法配备公务用枪的人员，丢失枪支不及时报告，造成严重后果的，处三年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百三十条　【非法携带枪支、弹药、管制刀具、危险物品危及公共安全罪】非法携带枪支、弹药、管制刀具或者爆炸性、易燃性、放射性、毒害性、腐蚀性物品，进入公共场所或者公共交通工具，危及公共安全，情节严重的，处三年以下有期徒刑、拘役或者管制。</p>
        <p class="indent">第一百三十一条　【重大飞行事故罪】航空人员违反规章制度，致使发生重大飞行事故，造成严重后果的，处三年以下有期徒刑或者拘役;造成飞机坠毁或者人员死亡的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">第一百三十二条　【铁路运营安全事故罪】铁路职工违反规章制度，致使发生铁路运营安全事故，造成严重后果的，处三年以下有期徒刑或者拘役;造成特别严重后果的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">第一百三十三条　【交通肇事罪】违反交通运输管理法规，因而发生重大事故，致人重伤、死亡或者使公私财产遭受重大损失的，处三年以下有期徒刑或者拘役;交通运输肇事后逃逸或者有其他特别恶劣情节的，处三年以上七年以下有期徒刑;因逃逸致人死亡的，处七年以上有期徒刑。</p>
        <p class="indent">第一百三十三条之一　【危险驾驶罪】在道路上驾驶机动车，有下列情形之一的，处拘役，并处罚金：</p>
        <p class="indent">(一)追逐竞驶，情节恶劣的;</p>
        <p class="indent">(二)醉酒驾驶机动车的;</p>
        <p class="indent">(三)从事校车业务或者旅客运输，严重超过额定乘员载客，或者严重超过规定时速行驶的;</p>
        <p class="indent">(四)违反危险化学品安全管理规定运输危险化学品，危及公共安全的。</p>
        <p class="indent">机动车所有人、管理人对前款第三项、第四项行为负有直接责任的，依照前款的规定处罚。</p>
        <p class="indent">有前两款行为，同时构成其他犯罪的，依照处罚较重的规定定罪处罚。</p>
        <p class="indent">第一百三十四条　【重大责任事故罪】在生产、作业中违反有关安全管理的规定，因而发生重大伤亡事故或者造成其他严重后果的，处三年以下有期徒刑或者拘役;情节特别恶劣的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">【强令违章冒险作业罪】强令他人违章冒险作业，因而发生重大伤亡事故或者造成其他严重后果的，处五年以下有期徒刑或者拘役;情节特别恶劣的，处五年以上有期徒刑。</p>
        <p class="indent">第一百三十五条　【重大劳动安全事故罪】安全生产设施或者安全生产条件不符合国家规定，因而发生重大伤亡事故或者造成其他严重后果的，对直接负责的主管人员和其他直接责任人员，处三年以下有期徒刑或者拘役;情节特别恶劣的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">第一百三十五条之一　【大型群众性活动重大安全事故罪】举办大型群众性活动违反安全管理规定，因而发生重大伤亡事故或者造成其他严重后果的，对直接负责的主管人员和其他直接责任人员，处三年以下有期徒刑或者拘役;情节特别恶劣的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">第一百三十六条　【危险物品肇事罪】违反爆炸性、易燃性、放射性、毒害性、腐蚀性物品的管理规定，在生产、储存、运输、使用中发生重大事故，造成严重后果的，处三年以下有期徒刑或者拘役;后果特别严重的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">第一百三十七条　【工程重大安全事故罪】建设单位、设计单位、施工单位、工程监理单位违反国家规定，降低工程质量标准，造成重大安全事故的，对直接责任人员，处五年以下有期徒刑或者拘役，并处罚金;后果特别严重的，处五年以上十年以下有期徒刑，并处罚金。</p>
        <p class="indent">第一百三十八条　【教育设施重大安全事故罪】明知校舍或者教育教学设施有危险，而不采取措施或者不及时报告，致使发生重大伤亡事故的，对直接责任人员，处三年以下有期徒刑或者拘役;后果特别严重的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">第一百三十九条　【消防责任事故罪】违反消防管理法规，经消防监督机构通知采取改正措施而拒绝执行，造成严重后果的，对直接责任人员，处三年以下有期徒刑或者拘役;后果特别严重的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">第一百三十九条之一　【不报、谎报安全事故罪】在安全事故发生后，负有报告职责的人员不报或者谎报事故情况，贻误事故抢救，情节严重的，处三年以下有期徒刑或者拘役;情节特别严重的，处三年以上七年以下有期徒刑。</p>
        <p class="bold indent">第三章　破坏社会主义市场经济秩序罪</p>
        <p class="indent">第一节　生产、销售伪劣商品罪</p>
        <p class="indent">第一百四十条　【生产、销售伪劣产品罪】生产者、销售者在产品中掺杂、掺假，以假充真，以次充好或者以不合格产品冒充合格产品，销售金额五万元以上不满二十万元的，处二年以下有期徒刑或者拘役，并处或者单处销售金额百分之五十以上二倍以下罚金;销售金额二十万元以上不满五十万元的，处二年以上七年以下有期徒刑，并处销售金额百分之五十以上二倍以下罚金;销售金额五十万元以上不满二百万元的，处七年以上有期徒刑，并处销售金额百分之五十以上二倍以下罚金;销售金额二百万元以上的，处十五年有期徒刑或者无期徒刑，并处销售金额百分之五十以上二倍以下罚金或者没收财产。</p>
        <p class="indent">第一百四十一条　【生产、销售假药罪】生产、销售假药的，处三年以下有期徒刑或者拘役，并处罚金;对人体健康造成严重危害或者有其他严重情节的，处三年以上十年以下有期徒刑，并处罚金;致人死亡或者有其他特别严重情节的，处十年以上有期徒刑、无期徒刑或者死刑，并处罚金或者没收财产。</p>
        <p class="indent">本条所称假药，是指依照《中华人民共和国药品管理法》的规定属于假药和按假药处理的药品、非药品。</p>
        <p class="indent">第一百四十二条　【生产、销售劣药罪】生产、销售劣药，对人体健康造成严重危害的，处三年以上十年以下有期徒刑，并处销售金额百分之五十以上二倍以下罚金;后果特别严重的，处十年以上有期徒刑或者无期徒刑，并处销售金额百分之五十以上二倍以下罚金或者没收财产。</p>
        <p class="indent">本条所称劣药，是指依照《中华人民共和国药品管理法》的规定属于劣药的药品。</p>
        <p class="indent">第一百四十三条　【生产、销售不符合安全标准的食品罪】生产、销售不符合食品安全标准的食品，足以造成严重食物中毒事故或者其他严重食源性疾病的，处三年以下有期徒刑或者拘役，并处罚金;对人体健康造成严重危害或者有其他严重情节的，处三年以上七年以下有期徒刑，并处罚金;后果特别严重的，处七年以上有期徒刑或者无期徒刑，并处罚金或者没收财产。</p>
        <p class="indent">第一百四十四条　【生产、销售有毒、有害食品罪】在生产、销售的食品中掺入有毒、有害的非食品原料的，或者销售明知掺有有毒、有害的非食品原料的食品的，处五年以下有期徒刑，并处罚金;对人体健康造成严重危害或者有其他严重情节的，处五年以上十年以下有期徒刑，并处罚金;致人死亡或者有其他特别严重情节的，依照本法第一百四十一条的规定处罚。</p>
        <p class="indent">第一百四十五条　【生产、销售不符合标准的医用器材罪】生产不符合保障人体健康的国家标准、行业标准的医疗器械、医用卫生材料，或者销售明知是不符合保障人体健康的国家标准、行业标准的医疗器械、医用卫生材料，足以严重危害人体健康的，处三年以下有期徒刑或者拘役，并处销售金额百分之五十以上二倍以下罚金;对人体健康造成严重危害的，处三年以上十年以下有期徒刑，并处销售金额百分之五十以上二倍以下罚金;后果特别严重的，处十年以上有期徒刑或者无期徒刑，并处销售金额百分之五十以上二倍以下罚金或者没收财产。</p>
        <p class="indent">第一百四十六条　【生产、销售不符合安全标准的产品罪】生产不符合保障人身、财产安全的国家标准、行业标准的电器、压力容器、易燃易爆产品或者其他不符合保障人身、财产安全的国家标准、行业标准的产品，或者销售明知是以上不符合保障人身、财产安全的国家标准、行业标准的产品，造成严重后果的，处五年以下有期徒刑，并处销售金额百分之五十以上二倍以下罚金;后果特别严重的，处五年以上有期徒刑，并处销售金额百分之五十以上二倍以下罚金。</p>
        <p class="indent">第一百四十七条　【生产、销售伪劣农药、兽药、化肥、种子罪】生产假农药、假兽药、假化肥，销售明知是假的或者失去使用效能的农药、兽药、化肥、种子，或者生产者、销售者以不合格的农药、兽药、化肥、种子冒充合格的农药、兽药、化肥、种子，使生产遭受较大损失的，处三年以下有期徒刑或者拘役，并处或者单处销售金额百分之五十以上二倍以下罚金;使生产遭受重大损失的，处三年以上七年以下有期徒刑，并处销售金额百分之五十以上二倍以下罚金;使生产遭受特别重大损失的，处七年以上有期徒刑或者无期徒刑，并处销售金额百分之五十以上二倍以下罚金或者没收财产。</p>
        <p class="indent">第一百四十八条　【生产、销售不符合卫生标准的化妆品罪】生产不符合卫生标准的化妆品，或者销售明知是不符合卫生标准的化妆品，造成严重后果的，处三年以下有期徒刑或者拘役，并处或者单处销售金额百分之五十以上二倍以下罚金。</p>
        <p class="indent">第一百四十九条　【对生产、销售伪劣商品行为的法条适用】生产、销售本节第一百四十一条至第一百四十八条所列产品，不构成各该条规定的犯罪，但是销售金额在五万元以上的，依照本节第一百四十条的规定定罪处罚。</p>
        <p class="indent">生产、销售本节第一百四十一条至第一百四十八条所列产品，构成各该条规定的犯罪，同时又构成本节第一百四十条规定之罪的，依照处罚较重的规定定罪处罚。</p>
        <p class="indent">第一百五十条　【单位犯本节规定之罪的处理】单位犯本节第一百四十条至第一百四十八条规定之罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照各该条的规定处罚。</p>
        <p class="indent">第二节　走私罪</p>
        <p class="indent">第一百五十一条　【走私武器、弹药罪】【走私核材料罪】【走私假币罪】走私武器、弹药、核材料或者伪造的货币的，处七年以上有期徒刑，并处罚金或者没收财产;情节特别严重的，处无期徒刑，并处没收财产;情节较轻的，处三年以上七年以下有期徒刑，并处罚金。</p>
        <p class="indent">【走私文物罪】【走私贵重金属罪】【走私珍贵动物、珍贵动物制品罪】走私国家禁止出口的文物、黄金、白银和其他贵重金属或者国家禁止进出口的珍贵动物及其制品的，处五年以上十年以下有期徒刑，并处罚金;情节特别严重的，处十年以上有期徒刑或者无期徒刑，并处没收财产;情节较轻的，处五年以下有期徒刑，并处罚金。</p>
        <p class="indent">【走私国家禁止进出口的货物、物品罪】走私珍稀植物及其制品等国家禁止进出口的其他货物、物品的，处五年以下有期徒刑或者拘役，并处或者单处罚金;情节严重的，处五年以上有期徒刑，并处罚金。</p>
        <p class="indent">单位犯本条规定之罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照本条各款的规定处罚。</p>
        <p class="indent">第一百五十二条　【走私淫秽物品罪】以牟利或者传播为目的，走私淫秽的影片、录像带、录音带、图片、书刊或者其他淫秽物品的，处三年以上十年以下有期徒刑，并处罚金;情节严重的，处十年以上有期徒刑或者无期徒刑，并处罚金或者没收财产;情节较轻的，处三年以下有期徒刑、拘役或者管制，并处罚金。</p>
        <p class="indent">【走私废物罪】逃避海关监管将境外固体废物、液态废物和气态废物运输进境，情节严重的，处五年以下有期徒刑，并处或者单处罚金;情节特别严重的，处五年以上有期徒刑，并处罚金。</p>
        <p class="indent">单位犯前两款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前两款的规定处罚。</p>
        <p class="indent">第一百五十三条　【走私普通货物、物品罪】走私本法第一百五十一条、第一百五十二条、第三百四十七条规定以外的货物、物品的，根据情节轻重，分别依照下列规定处罚：</p>
        <p class="indent">(一)走私货物、物品偷逃应缴税额较大或者一年内曾因走私被给予二次行政处罚后又走私的，处三年以下有期徒刑或者拘役，并处偷逃应缴税额一倍以上五倍以下罚金。</p>
        <p class="indent">(二)走私货物、物品偷逃应缴税额巨大或者有其他严重情节的，处三年以上十年以下有期徒刑，并处偷逃应缴税额一倍以上五倍以下罚金。</p>
        <p class="indent">(三)走私货物、物品偷逃应缴税额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑或者无期徒刑，并处偷逃应缴税额一倍以上五倍以下罚金或者没收财产。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处三年以下有期徒刑或者拘役;情节严重的，处三年以上十年以下有期徒刑;情节特别严重的，处十年以上有期徒刑。</p>
        <p class="indent">对多次走私未经处理的，按照累计走私货物、物品的偷逃应缴税额处罚。</p>
        <p class="indent">第一百五十四条　【走私货物、物品罪的特殊形式】下列走私行为，根据本节规定构成犯罪的，依照本法第一百五十三条的规定定罪处罚：</p>
        <p class="indent">(一)未经海关许可并且未补缴应缴税额，擅自将批准进口的来料加工、来件装配、补偿贸易的原材料、零件、制成品、设备等保税货物，在境内销售牟利的;</p>
        <p class="indent">(二)未经海关许可并且未补缴应缴税额，擅自将特定减税、免税进口的货物、物品，在境内销售牟利的。</p>
        <p class="indent">第一百五十五条　【以走私罪论处的间接走私行为】下列行为，以走私罪论处，依照本节的有关规定处罚：</p>
        <p class="indent">(一)直接向走私人非法收购国家禁止进口物品的，或者直接向走私人非法收购走私进口的其他货物、物品，数额较大的;</p>
        <p class="indent">(二)在内海、领海、界河、界湖运输、收购、贩卖国家禁止进出口物品的，或者运输、收购、贩卖国家限制进出口货物、物品，数额较大，没有合法证明的。</p>
        <p class="indent">第一百五十六条　【走私共犯】与走私罪犯通谋，为其提供贷款、资金、帐号、发票、证明，或者为其提供运输、保管、邮寄或者其他方便的，以走私罪的共犯论处。</p>
        <p class="indent">第一百五十七条　【武装掩护走私、抗拒缉私的规定】武装掩护走私的，依照本法第一百五十一条第一款的规定从重处罚。</p>
        <p class="indent">以暴力、威胁方法抗拒缉私的，以走私罪和本法第二百七十七条规定的阻碍国家机关工作人员依法执行职务罪，依照数罪并罚的规定处罚。</p>
        <p class="indent">第三节　妨害对公司、企业的管理秩序罪</p>
        <p class="indent">第一百五十八条　【虚报注册资本罪】申请公司登记使用虚假证明文件或者采取其他欺诈手段虚报注册资本，欺骗公司登记主管部门，取得公司登记，虚报注册资本数额巨大、后果严重或者有其他严重情节的，处三年以下有期徒刑或者拘役，并处或者单处虚报注册资本金额百分之一以上百分之五以下罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处三年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百五十九条　【虚假出资、抽逃出资罪】公司发起人、股东违反公司法的规定未交付货币、实物或者未转移财产权，虚假出资，或者在公司成立后又抽逃其出资，数额巨大、后果严重或者有其他严重情节的，处五年以下有期徒刑或者拘役，并处或者单处虚假出资金额或者抽逃出资金额百分之二以上百分之十以下罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百六十条　【欺诈发行股票、债券罪】在招股说明书、认股书、公司、企业债券募集办法中隐瞒重要事实或者编造重大虚假内容，发行股票或者公司、企业债券，数额巨大、后果严重或者有其他严重情节的，处五年以下有期徒刑或者拘役，并处或者单处非法募集资金金额百分之一以上百分之五以下罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百六十一条　【违规披露、不披露重要信息罪】依法负有信息披露义务的公司、企业向股东和社会公众提供虚假的或者隐瞒重要事实的财务会计报告，或者对依法应当披露的其他重要信息不按照规定披露，严重损害股东或者其他人利益，或者有其他严重情节的，对其直接负责的主管人员和其他直接责任人员，处三年以下有期徒刑或者拘役，并处或者单处二万元以上二十万元以下罚金。</p>
        <p class="indent">第一百六十二条　【妨害清算罪】公司、企业进行清算时，隐匿财产，对资产负债表或者财产清单作虚伪记载或者在未清偿债务前分配公司、企业财产，严重损害债权人或者其他人利益的，对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑或者拘役，并处或者单处二万元以上二十万元以下罚金。</p>
        <p class="indent">第一百六十二条之一　【隐匿、故意销毁会计凭证、会计账簿、财务会计报告罪】隐匿或者故意销毁依法应当保存的会计凭证、会计账簿、财务会计报告，情节严重的，处五年以下有期徒刑或者拘役，并处或者单处二万元以上二十万元以下罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前款的规定处罚。</p>
        <p class="indent">第一百六十二条之二　【虚假破产罪】公司、企业通过隐匿财产、承担虚构的债务或者以其他方法转移、处分财产，实施虚假破产，严重损害债权人或者其他人利益的，对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑或者拘役，并处或者单处二万元以上二十万元以下罚金。</p>
        <p class="indent">第一百六十三条　【非国家工作人员受贿罪】公司、企业或者其他单位的工作人员利用职务上的便利，索取他人财物或者非法收受他人财物，为他人谋取利益，数额较大的，处五年以下有期徒刑或者拘役;数额巨大的，处五年以上有期徒刑，可以并处没收财产。</p>
        <p class="indent">公司、企业或者其他单位的工作人员在经济往来中，利用职务上的便利，违反国家规定，收受各种名义的回扣、手续费，归个人所有的，依照前款的规定处罚。</p>
        <p class="indent">国有公司、企业或者其他国有单位中从事公务的人员和国有公司、企业或者其他国有单位委派到非国有公司、企业以及其他单位从事公务的人员有前两款行为的，依照本法第三百八十五条、第三百八十六条的规定定罪处罚。</p>
        <p class="indent">第一百六十四条　【对非国家工作人员行贿罪】为谋取不正当利益，给予公司、企业或者其他单位的工作人员以财物，数额较大的，处三年以下有期徒刑或者拘役，并处罚金;数额巨大的，处三年以上十年以下有期徒刑，并处罚金。</p>
        <p class="indent">【对外国公职人员、国际公共组织官员行贿罪】为谋取不正当商业利益，给予外国公职人员或者国际公共组织官员以财物的，依照前款的规定处罚。</p>
        <p class="indent">单位犯前两款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照第一款的规定处罚。</p>
        <p class="indent">行贿人在被追诉前主动交待行贿行为的，可以减轻处罚或者免除处罚。</p>
        <p class="indent">第一百六十五条　【非法经营同类营业罪】国有公司、企业的董事、经理利用职务便利，自己经营或者为他人经营与其所任职公司、企业同类的营业，获取非法利益，数额巨大的，处三年以下有期徒刑或者拘役，并处或者单处罚金;数额特别巨大的，处三年以上七年以下有期徒刑，并处罚金。</p>
        <p class="indent">第一百六十六条　【为亲友非法牟利罪】国有公司、企业、事业单位的工作人员，利用职务便利，有下列情形之一，使国家利益遭受重大损失的，处三年以下有期徒刑或者拘役，并处或者单处罚金;致使国家利益遭受特别重大损失的，处三年以上七年以下有期徒刑，并处罚金：</p>
        <p class="indent">(一)将本单位的盈利业务交由自己的亲友进行经营的;</p>
        <p class="indent">(二)以明显高于市场的价格向自己的亲友经营管理的单位采购商品或者以明显低于市场的价格向自己的亲友经营管理的单位销售商品的;</p>
        <p class="indent">(三)向自己的亲友经营管理的单位采购不合格商品的。</p>
        <p class="indent">第一百六十七条　【签订、履行合同失职被骗罪】国有公司、企业、事业单位直接负责的主管人员，在签订、履行合同过程中，因严重不负责任被诈骗，致使国家利益遭受重大损失的，处三年以下有期徒刑或者拘役;致使国家利益遭受特别重大损失的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">第一百六十八条　【国有公司、企业、事业单位人员失职罪】 【国有公司、企业、事业单位人员滥用职权罪】国有公司、企业的工作人员，由于严重不负责任或者滥用职权，造成国有公司、企业破产或者严重损失，致使国家利益遭受重大损失的，处三年以下有期徒刑或者拘役;致使国家利益遭受特别重大损失的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">国有事业单位的工作人员有前款行为，致使国家利益遭受重大损失的，依照前款的规定处罚。</p>
        <p class="indent">国有公司、企业、事业单位的工作人员，徇私舞弊，犯前两款罪的，依照第一款的规定从重处罚。</p>
        <p class="indent">第一百六十九条　【徇私舞弊低价折股、出售国有资产罪】国有公司、企业或者其上级主管部门直接负责的主管人员，徇私舞弊，将国有资产低价折股或者低价出售，致使国家利益遭受重大损失的，处三年以下有期徒刑或者拘役;致使国家利益遭受特别重大损失的，处三年以上七年以下有期徒刑。</p>
        <p class="indent">第一百六十九条之一　【背信损害上市公司利益罪】上市公司的董事、监事、高级管理人员违背对公司的忠实义务，利用职务便利，操纵上市公司从事下列行为之一，致使上市公司利益遭受重大损失的，处三年以下有期徒刑或者拘役，并处或者单处罚金;致使上市公司利益遭受特别重大损失的，处三年以上七年以下有期徒刑，并处罚金：</p>
        <p class="indent">(一)无偿向其他单位或者个人提供资金、商品、服务或者其他资产的;</p>
        <p class="indent">(二)以明显不公平的条件，提供或者接受资金、商品、服务或者其他资产的;</p>
        <p class="indent">(三)向明显不具有清偿能力的单位或者个人提供资金、商品、服务或者其他资产的;</p>
        <p class="indent">(四)为明显不具有清偿能力的单位或者个人提供担保，或者无正当理由为其他单位或者个人提供担保的;</p>
        <p class="indent">(五)无正当理由放弃债权、承担债务的;</p>
        <p class="indent">(六)采用其他方式损害上市公司利益的。</p>
        <p class="indent">上市公司的控股股东或者实际控制人，指使上市公司董事、监事、高级管理人员实施前款行为的，依照前款的规定处罚。</p>
        <p class="indent">犯前款罪的上市公司的控股股东或者实际控制人是单位的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照第一款的规定处罚。</p>
        <p class="indent">第四节　破坏金融管理秩序罪</p>
        <p class="indent">第一百七十条　【伪造货币罪】伪造货币的，处三年以上十年以下有期徒刑，并处罚金;有下列情形之一的，处十年以上有期徒刑或者无期徒刑，并处罚金或者没收财产：</p>
        <p class="indent">(一)伪造货币集团的首要分子;</p>
        <p class="indent">(二)伪造货币数额特别巨大的;</p>
        <p class="indent">(三)有其他特别严重情节的。</p>
        <p class="indent">第一百七十一条　【出售、购买、运输假币罪】出售、购买伪造的货币或者明知是伪造的货币而运输，数额较大的，处三年以下有期徒刑或者拘役，并处二万元以上二十万元以下罚金;数额巨大的，处三年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金;数额特别巨大的，处十年以上有期徒刑或者无期徒刑，并处五万元以上五十万元以下罚金或者没收财产。</p>
        <p class="indent">【金融工作人员购买假币、以假币换取货币罪】银行或者其他金融机构的工作人员购买伪造的货币或者利用职务上的便利，以伪造的货币换取货币的，处三年以上十年以下有期徒刑，并处二万元以上二十万元以下罚金;数额巨大或者有其他严重情节的，处十年以上有期徒刑或者无期徒刑，并处二万元以上二十万元以下罚金或者没收财产;情节较轻的，处三年以下有期徒刑或者拘役，并处或者单处一万元以上十万元以下罚金。</p>
        <p class="indent">伪造货币并出售或者运输伪造的货币的，依照本法第一百七十条的规定定罪从重处罚。</p>
        <p class="indent">第一百七十二条　【持有、使用假币罪】明知是伪造的货币而持有、使用，数额较大的，处三年以下有期徒刑或者拘役，并处或者单处一万元以上十万元以下罚金;数额巨大的，处三年以上十年以下有期徒刑，并处二万元以上二十万元以下罚金;数额特别巨大的，处十年以上有期徒刑，并处五万元以上五十万元以下罚金或者没收财产。</p>
        <p class="indent">第一百七十三条　【变造货币罪】变造货币，数额较大的，处三年以下有期徒刑或者拘役，并处或者单处一万元以上十万元以下罚金;数额巨大的，处三年以上十年以下有期徒刑，并处二万元以上二十万元以下罚金。</p>
        <p class="indent">第一百七十四条　【擅自设立金融机构罪】未经国家有关主管部门批准，擅自设立商业银行、证券交易所、期货交易所、证券公司、期货经纪公司、保险公司或者其他金融机构的，处三年以下有期徒刑或者拘役，并处或者单处二万元以上二十万元以下罚金;情节严重的，处三年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金。</p>
        <p class="indent">【伪造、变造、转让金融机构经营许可证、批准文件罪】伪造、变造、转让商业银行、证券交易所、期货交易所、证券公司、期货经纪公司、保险公司或者其他金融机构的经营许可证或者批准文件的，依照前款的规定处罚。</p>
        <p class="indent">单位犯前两款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照第一款的规定处罚。</p>
        <p class="indent">第一百七十五条　【高利转贷罪】以转贷牟利为目的，套取金融机构信贷资金高利转贷他人，违法所得数额较大的，处三年以下有期徒刑或者拘役，并处违法所得一倍以上五倍以下罚金;数额巨大的，处三年以上七年以下有期徒刑，并处违法所得一倍以上五倍以下罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处三年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百七十五条之一　【骗取贷款、票据承兑、金融票证罪】以欺骗手段取得银行或者其他金融机构贷款、票据承兑、信用证、保函等，给银行或者其他金融机构造成重大损失或者有其他严重情节的，处三年以下有期徒刑或者拘役，并处或者单处罚金;给银行或者其他金融机构造成特别重大损失或者有其他特别严重情节的，处三年以上七年以下有期徒刑，并处罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前款的规定处罚。</p>
        <p class="indent">第一百七十六条　【非法吸收公众存款罪】非法吸收公众存款或者变相吸收公众存款，扰乱金融秩序的，处三年以下有期徒刑或者拘役，并处或者单处二万元以上二十万元以下罚金;数额巨大或者有其他严重情节的，处三年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前款的规定处罚。</p>
        <p class="indent">第一百七十七条　【伪造、变造金融票证罪】有下列情形之一，伪造、变造金融票证的，处五年以下有期徒刑或者拘役，并处或者单处二万元以上二十万元以下罚金;情节严重的，处五年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金;情节特别严重的，处十年以上有期徒刑或者无期徒刑，并处五万元以上五十万元以下罚金或者没收财产：</p>
        <p class="indent">(一)伪造、变造汇票、本票、支票的;</p>
        <p class="indent">(二)伪造、变造委托收款凭证、汇款凭证、银行存单等其他银行结算凭证的;</p>
        <p class="indent">(三)伪造、变造信用证或者附随的单据、文件的;</p>
        <p class="indent">(四)伪造信用卡的。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前款的规定处罚。</p>
        <p class="indent">第一百七十七条之一　【妨害信用卡管理罪】有下列情形之一，妨害信用卡管理的，处三年以下有期徒刑或者拘役，并处或者单处一万元以上十万元以下罚金;数量巨大或者有其他严重情节的，处三年以上十年以下有期徒刑，并处二万元以上二十万元以下罚金：</p>
        <p class="indent">(一)明知是伪造的信用卡而持有、运输的，或者明知是伪造的空白信用卡而持有、运输，数量较大的;</p>
        <p class="indent">(二)非法持有他人信用卡，数量较大的;</p>
        <p class="indent">(三)使用虚假的身份证明骗领信用卡的;</p>
        <p class="indent">(四)出售、购买、为他人提供伪造的信用卡或者以虚假的身份证明骗领的信用卡的。</p>
        <p class="indent">【窃取、收买、非法提供信用卡信息罪】窃取、收买或者非法提供他人信用卡信息资料的，依照前款规定处罚。</p>
        <p class="indent">银行或者其他金融机构的工作人员利用职务上的便利，犯第二款罪的，从重处罚。</p>
        <p class="indent">第一百七十八条　【伪造、变造国家有价证券罪】伪造、变造国库券或者国家发行的其他有价证券，数额较大的，处三年以下有期徒刑或者拘役，并处或者单处二万元以上二十万元以下罚金;数额巨大的，处三年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金;数额特别巨大的，处十年以上有期徒刑或者无期徒刑，并处五万元以上五十万元以下罚金或者没收财产。</p>
        <p class="indent">【伪造、变造股票、公司、企业债券罪】伪造、变造股票或者公司、企业债券，数额较大的，处三年以下有期徒刑或者拘役，并处或者单处一万元以上十万元以下罚金;数额巨大的，处三年以上十年以下有期徒刑，并处二万元以上二十万元以下罚金。</p>
        <p class="indent">单位犯前两款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前两款的规定处罚。</p>
        <p class="indent">第一百七十九条　【擅自发行股票、公司、企业债券罪】未经国家有关主管部门批准，擅自发行股票或者公司、企业债券，数额巨大、后果严重或者有其他严重情节的，处五年以下有期徒刑或者拘役，并处或者单处非法募集资金金额百分之一以上百分之五以下罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百八十条　【内幕交易、泄露内幕信息罪】证券、期货交易内幕信息的知情人员或者非法获取证券、期货交易内幕信息的人员，在涉及证券的发行，证券、期货交易或者其他对证券、期货交易价格有重大影响的信息尚未公开前，买入或者卖出该证券，或者从事与该内幕信息有关的期货交易，或者泄露该信息，或者明示、暗示他人从事上述交易活动，情节严重的，处五年以下有期徒刑或者拘役，并处或者单处违法所得一倍以上五倍以下罚金;情节特别严重的，处五年以上十年以下有期徒刑，并处违法所得一倍以上五倍以下罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑或者拘役。</p>
        <p class="indent">内幕信息、知情人员的范围，依照法律、行政法规的规定确定。</p>
        <p class="indent">【利用未公开信息交易罪】证券交易所、期货交易所、证券公司、期货经纪公司、基金管理公司、商业银行、保险公司等金融机构的从业人员以及有关监管部门或者行业协会的工作人员，利用因职务便利获取的内幕信息以外的其他未公开的信息，违反规定，从事与该信息相关的证券、期货交易活动，或者明示、暗示他人从事相关交易活动，情节严重的，依照第一款的规定处罚。</p>
        <p class="indent">第一百八十一条　【编造并传播证券、期货交易虚假信息罪】编造并且传播影响证券、期货交易的虚假信息，扰乱证券、期货交易市场，造成严重后果的，处五年以下有期徒刑或者拘役，并处或者单处一万元以上十万元以下罚金。</p>
        <p class="indent">【诱骗投资者买卖证券、期货合约罪】证券交易所、期货交易所、证券公司、期货经纪公司的从业人员，证券业协会、期货业协会或者证券期货监督管理部门的工作人员，故意提供虚假信息或者伪造、变造、销毁交易记录，诱骗投资者买卖证券、期货合约，造成严重后果的，处五年以下有期徒刑或者拘役，并处或者单处一万元以上十万元以下罚金;情节特别恶劣的，处五年以上十年以下有期徒刑，并处二万元以上二十万元以下罚金。</p>
        <p class="indent">单位犯前两款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑或者拘役。</p>
        <p class="indent">第一百八十二条　【操纵证券、期货市场罪】有下列情形之一，操纵证券、期货市场，情节严重的，处五年以下有期徒刑或者拘役，并处或者单处罚金;情节特别严重的，处五年以上十年以下有期徒刑，并处罚金：</p>
        <p class="indent">(一)单独或者合谋，集中资金优势、持股或者持仓优势或者利用信息优势联合或者连续买卖，操纵证券、期货交易价格或者证券、期货交易量的;</p>
        <p class="indent">(二)与他人串通，以事先约定的时间、价格和方式相互进行证券、期货交易，影响证券、期货交易价格或者证券、期货交易量的;</p>
        <p class="indent">(三)在自己实际控制的账户之间进行证券交易，或者以自己为交易对象，自买自卖期货合约，影响证券、期货交易价格或者证券、期货交易量的;</p>
        <p class="indent">(四)以其他方法操纵证券、期货市场的。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前款的规定处罚。</p>
        <p class="indent">第一百八十三条　【职务侵占罪】保险公司的工作人员利用职务上的便利，故意编造未曾发生的保险事故进行虚假理赔，骗取保险金归自己所有的，依照本法第二百七十一条的规定定罪处罚。</p>
        <p class="indent">【贪污罪】国有保险公司工作人员和国有保险公司委派到非国有保险公司从事公务的人员有前款行为的，依照本法第三百八十二条、第三百八十三条的规定定罪处罚。</p>
        <p class="indent">第一百八十四条　【非国家工作人员受贿罪】银行或者其他金融机构的工作人员在金融业务活动中索取他人财物或者非法收受他人财物，为他人谋取利益的，或者违反国家规定，收受各种名义的回扣、手续费，归个人所有的，依照本法第一百六十三条的规定定罪处罚。</p>
        <p class="indent">【受贿罪】国有金融机构工作人员和国有金融机构委派到非国有金融机构从事公务的人员有前款行为的，依照本法第三百八十五条、第三百八十六条的规定定罪处罚。</p>
        <p class="indent">第一百八十五条　【挪用资金罪】商业银行、证券交易所、期货交易所、证券公司、期货经纪公司、保险公司或者其他金融机构的工作人员利用职务上的便利，挪用本单位或者客户资金的，依照本法第二百七十二条的规定定罪处罚。</p>
        <p class="indent">【挪用公款罪】国有商业银行、证券交易所、期货交易所、证券公司、期货经纪公司、保险公司或者其他国有金融机构的工作人员和国有商业银行、证券交易所、期货交易所、证券公司、期货经纪公司、保险公司或者其他国有金融机构委派到前款规定中的非国有机构从事公务的人员有前款行为的，依照本法第三百八十四条的规定定罪处罚。</p>
        <p class="indent">第一百八十五条之一　【背信运用受托财产罪】商业银行、证券交易所、期货交易所、证券公司、期货经纪公司、保险公司或者其他金融机构，违背受托义务，擅自运用客户资金或者其他委托、信托的财产，情节严重的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处三年以下有期徒刑或者拘役，并处三万元以上三十万元以下罚金;情节特别严重的，处三年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金。</p>
        <p class="indent">【违法运用资金罪】社会保障基金管理机构、住房公积金管理机构等公众资金管理机构，以及保险公司、保险资产管理公司、证券投资基金管理公司，违反国家规定运用资金的，对其直接负责的主管人员和其他直接责任人员，依照前款的规定处罚。</p>
        <p class="indent">第一百八十六条　【违法发放贷款罪】银行或者其他金融机构的工作人员违反国家规定发放贷款，数额巨大或者造成重大损失的，处五年以下有期徒刑或者拘役，并处一万元以上十万元以下罚金;数额特别巨大或者造成特别重大损失的，处五年以上有期徒刑，并处二万元以上二十万元以下罚金。</p>
        <p class="indent">银行或者其他金融机构的工作人员违反国家规定，向关系人发放贷款的，依照前款的规定从重处罚。</p>
        <p class="indent">单位犯前两款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前两款的规定处罚。</p>
        <p class="indent">关系人的范围，依照《中华人民共和国商业银行法》和有关金融法规确定。</p>
        <p class="indent">第一百八十七条　【吸收客户资金不入账罪】银行或者其他金融机构的工作人员吸收客户资金不入账，数额巨大或者造成重大损失的，处五年以下有期徒刑或者拘役，并处二万元以上二十万元以下罚金;数额特别巨大或者造成特别重大损失的，处五年以上有期徒刑，并处五万元以上五十万元以下罚金。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前款的规定处罚。</p>
        <p class="indent">第一百八十八条　【违规出具金融票证罪】银行或者其他金融机构的工作人员违反规定，为他人出具信用证或者其他保函、票据、存单、资信证明，情节严重的，处五年以下有期徒刑或者拘役;情节特别严重的，处五年以上有期徒刑。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前款的规定处罚。</p>
        <p class="indent">第一百八十九条　【对违法票据承兑、付款、保证罪】银行或者其他金融机构的工作人员在票据业务中，对违反票据法规定的票据予以承兑、付款或者保证，造成重大损失的，处五年以下有期徒刑或者拘役;造成特别重大损失的，处五年以上有期徒刑。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，依照前款的规定处罚。</p>
        <p class="indent">第一百九十条　【逃汇罪】公司、企业或者其他单位，违反国家规定，擅自将外汇存放境外，或者将境内的外汇非法转移到境外，数额较大的，对单位判处逃汇数额百分之五以上百分之三十以下罚金，并对其直接负责的主管人员和其他直接责任人员处五年以下有期徒刑或者拘役;数额巨大或者有其他严重情节的，对单位判处逃汇数额百分之五以上百分之三十以下罚金，并对其直接负责的主管人员和其他直接责任人员处五年以上有期徒刑。</p>
        <p class="indent">第一百九十一条　【洗钱罪】明知是毒品犯罪、黑社会性质的组织犯罪、恐怖活动犯罪、走私犯罪、贪污贿赂犯罪、破坏金融管理秩序犯罪、金融诈骗犯罪的所得及其产生的收益，为掩饰、隐瞒其来源和性质，有下列行为之一的，没收实施以上犯罪的所得及其产生的收益，处五年以下有期徒刑或者拘役，并处或者单处洗钱数额百分之五以上百分之二十以下罚金;情节严重的，处五年以上十年以下有期徒刑，并处洗钱数额百分之五以上百分之二十以下罚金：</p>
        <p class="indent">(一)提供资金账户的;</p>
        <p class="indent">(二)协助将财产转换为现金、金融票据、有价证券的;</p>
        <p class="indent">(三)通过转账或者其他结算方式协助资金转移的;</p>
        <p class="indent">(四)协助将资金汇往境外的;</p>
        <p class="indent">(五)以其他方法掩饰、隐瞒犯罪所得及其收益的来源和性质的。</p>
        <p class="indent">单位犯前款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑或者拘役;情节严重的，处五年以上十年以下有期徒刑。</p>
        <p class="indent">第五节　金融诈骗罪</p>
        <p class="indent">第一百九十二条　【集资诈骗罪】以非法占有为目的，使用诈骗方法非法集资，数额较大的，处五年以下有期徒刑或者拘役，并处二万元以上二十万元以下罚金;数额巨大或者有其他严重情节的，处五年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金;数额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑或者无期徒刑，并处五万元以上五十万元以下罚金或者没收财产。</p>
        <p class="indent">第一百九十三条　【贷款诈骗罪】有下列情形之一，以非法占有为目的，诈骗银行或者其他金融机构的贷款，数额较大的，处五年以下有期徒刑或者拘役，并处二万元以上二十万元以下罚金;数额巨大或者有其他严重情节的，处五年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金;数额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑或者无期徒刑，并处五万元以上五十万元以下罚金或者没收财产：</p>
        <p class="indent">(一)编造引进资金、项目等虚假理由的;</p>
        <p class="indent">(二)使用虚假的经济合同的;</p>
        <p class="indent">(三)使用虚假的证明文件的;</p>
        <p class="indent">(四)使用虚假的产权证明作担保或者超出抵押物价值重复担保的;</p>
        <p class="indent">(五)以其他方法诈骗贷款的。</p>
        <p class="indent">第一百九十四条　【票据诈骗罪】有下列情形之一，进行金融票据诈骗活动，数额较大的，处五年以下有期徒刑或者拘役，并处二万元以上二十万元以下罚金;数额巨大或者有其他严重情节的，处五年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金;数额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑或者无期徒刑，并处五万元以上五十万元以下罚金或者没收财产：</p>
        <p class="indent">(一)明知是伪造、变造的汇票、本票、支票而使用的;</p>
        <p class="indent">(二)明知是作废的汇票、本票、支票而使用的;</p>
        <p class="indent">(三)冒用他人的汇票、本票、支票的;</p>
        <p class="indent">(四)签发空头支票或者与其预留印鉴不符的支票，骗取财物的;</p>
        <p class="indent">(五)汇票、本票的出票人签发无资金保证的汇票、本票或者在出票时作虚假记载，骗取财物的。</p>
        <p class="indent">【金融凭证诈骗罪】使用伪造、变造的委托收款凭证、汇款凭证、银行存单等其他银行结算凭证的，依照前款的规定处罚。</p>
        <p class="indent">第一百九十五条　【信用证诈骗罪】有下列情形之一，进行信用证诈骗活动的，处五年以下有期徒刑或者拘役，并处二万元以上二十万元以下罚金;数额巨大或者有其他严重情节的，处五年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金;数额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑或者无期徒刑，并处五万元以上五十万元以下罚金或者没收财产：</p>
        <p class="indent">(一)使用伪造、变造的信用证或者附随的单据、文件的;</p>
        <p class="indent">(二)使用作废的信用证的;</p>
        <p class="indent">(三)骗取信用证的;</p>
        <p class="indent">(四)以其他方法进行信用证诈骗活动的。</p>
        <p class="indent">第一百九十六条　【信用卡诈骗罪】有下列情形之一，进行信用卡诈骗活动，数额较大的，处五年以下有期徒刑或者拘役，并处二万元以上二十万元以下罚金;数额巨大或者有其他严重情节的，处五年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金;数额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑或者无期徒刑，并处五万元以上五十万元以下罚金或者没收财产：</p>
        <p class="indent">(一)使用伪造的信用卡，或者使用以虚假的身份证明骗领的信用卡的;</p>
        <p class="indent">(二)使用作废的信用卡的;</p>
        <p class="indent">(三)冒用他人信用卡的;</p>
        <p class="indent">(四)恶意透支的。</p>
        <p class="indent">前款所称恶意透支，是指持卡人以非法占有为目的，超过规定限额或者规定期限透支，并且经发卡银行催收后仍不归还的行为。</p>
        <p class="indent">【盗窃罪】盗窃信用卡并使用的，依照本法第二百六十四条的规定定罪处罚。</p>
        <p class="indent">第一百九十七条　【有价证券诈骗罪】使用伪造、变造的国库券或者国家发行的其他有价证券，进行诈骗活动，数额较大的，处五年以下有期徒刑或者拘役，并处二万元以上二十万元以下罚金;数额巨大或者有其他严重情节的，处五年以上十年以下有期徒刑，并处五万元以上五十万元以下罚金;数额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑或者无期徒刑，并处五万元以上五十万元以下罚金或者没收财产。</p>
        <p class="indent">第一百九十八条　【保险诈骗罪】有下列情形之一，进行保险诈骗活动，数额较大的，处五年以下有期徒刑或者拘役，并处一万元以上十万元以下罚金;数额巨大或者有其他严重情节的，处五年以上十年以下有期徒刑，并处二万元以上二十万元以下罚金;数额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑，并处二万元以上二十万元以下罚金或者没收财产：</p>
        <p class="indent">(一)投保人故意虚构保险标的，骗取保险金的;</p>
        <p class="indent">(二)投保人、被保险人或者受益人对发生的保险事故编造虚假的原因或者夸大损失的程度，骗取保险金的;</p>
        <p class="indent">(三)投保人、被保险人或者受益人编造未曾发生的保险事故，骗取保险金的;</p>
        <p class="indent">(四)投保人、被保险人故意造成财产损失的保险事故，骗取保险金的;</p>
        <p class="indent">(五)投保人、受益人故意造成被保险人死亡、伤残或者疾病，骗取保险金的。</p>
        <p class="indent">有前款第四项、第五项所列行为，同时构成其他犯罪的，依照数罪并罚的规定处罚。</p>
        <p class="indent">单位犯第一款罪的，对单位判处罚金，并对其直接负责的主管人员和其他直接责任人员，处五年以下有期徒刑或者拘役;数额巨大或者有其他严重情节的，处五年以上十年以下有期徒刑;数额特别巨大或者有其他特别严重情节的，处十年以上有期徒刑。</p>
        <p class="indent">保险事故的鉴定人、证明人、财产评估人故意提供虚假的证明文件，为他人诈骗提供条件的，以保险诈骗的共犯论处。</p>
